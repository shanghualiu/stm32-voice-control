/* ============================ 字库访问函数 ============================ */

/**
 * @brief  获取 6×8 字库中字符的点阵数据
 * @param  ch ASCII 字符
 * @return const uint8_t* 点阵数据指针 (6 字节)
 */
const uint8_t* Font_Get_6x8(uint8_t ch)
{
    if (ch < ASCII_PRINTABLE_MIN || ch > ASCII_PRINTABLE_MAX)
    {
        ch = ' ';  /* 越界字符使用空格替代 */
    }

    uint8_t index = (uint8_t)(ch - ASCII_PRINTABLE_MIN);
    return Font_6x8[index];
}

/**
 * @brief  获取 8×16 字库中字符的点阵数据
 * @param  ch ASCII 字符
 * @return const uint8_t* 点阵数据指针 (16 字节)
 */
const uint8_t* Font_Get_8x16(uint8_t ch)
{
    if (ch < ASCII_PRINTABLE_MIN || ch > ASCII_PRINTABLE_MAX)
    {
        ch = ' ';
    }

    uint8_t index = (uint8_t)(ch - ASCII_PRINTABLE_MIN);
    return Font_8x16[index];
}
